<script setup>
import { onMounted, onBeforeUnmount, ref } from 'vue';
import { loadCSSFiles, unloadCSS } from "../../utils/cssLoader";

// 动态加载 CSS（只在当前页面生效）
const componentId = 'news-page';
// 使用 import.meta.url 来获取正确的路径
const cssFiles = [
  new URL("../../assets/css/common.css", import.meta.url).href,
  new URL("../../assets/css/zsystem_menu.css", import.meta.url).href,
  new URL("../../assets/css/news_home.css", import.meta.url).href,
  new URL("../../assets/css/bootstrap.css", import.meta.url).href,
  new URL("../../assets/css/owl.carousel.css", import.meta.url).href,
  new URL("../../assets/css/jqtree.css", import.meta.url).href,
  new URL("../../assets/css/footer.css", import.meta.url).href
];

// owl-demo轮播相关变量
let currentOwlSlide = ref(0);
let owlAutoPlayTimer = null;
const owlSlideInterval = 3000; // 自动播放间隔（毫秒）
const itemWidth = 320; // 每个item的宽度

// 初始化轮播
onMounted(() => {
  // 加载当前页面的 CSS
  loadCSSFiles(cssFiles, componentId);
  
  // viewport 已在路由守卫中设置，这里只需要确保设置正确（防止路由守卫执行失败）
  const isMobile = window.innerWidth <= 768;
  if (isMobile) {
    let meta = document.querySelector('meta[name="viewport"]');
    if (meta) {
      const deviceWidth = window.innerWidth || window.screen.width;
      const pageWidth = 1200;
      const calculatedScale = Math.min(deviceWidth / pageWidth, 1.0);
      const expectedContent = `width=${pageWidth}, initial-scale=${calculatedScale}, user-scalable=yes, maximum-scale=3.0, minimum-scale=0.1`;
      if (meta.content !== expectedContent) {
        meta.content = expectedContent;
      }
    }
  }
  
  initOwlCarousel();
});

// 页面卸载时恢复 viewport 为默认设置，并移除当前页面的 CSS
onBeforeUnmount(() => {
  // 卸载当前页面的 CSS
  unloadCSS(componentId);
  
  // 恢复 viewport 为默认设置
  let meta = document.querySelector('meta[name="viewport"]');
  if (meta) {
    meta.content = "width=device-width, initial-scale=1.0";
  }
});

function initOwlCarousel() {
  const owlDemo = document.getElementById('owl-demo');
  if (!owlDemo) return;

  const wrapper = owlDemo.querySelector('.owl-wrapper');
  const items = owlDemo.querySelectorAll('.owl-item');
  const pagination = owlDemo.querySelectorAll('.owl-pagination .owl-page');
  const prevBtn = owlDemo.querySelector('.owl-prev');
  const nextBtn = owlDemo.querySelector('.owl-next');
  const buttons = owlDemo.querySelector('.owl-buttons');

  if (!wrapper || items.length === 0) return;

  // 初始化：显示第一个slide
  currentOwlSlide.value = 0;
  updateOwlSlide(wrapper, pagination, items.length, 0);

  // 分页器点击事件
  pagination.forEach((page, index) => {
    page.addEventListener('click', (e) => {
      e.preventDefault();
      goToOwlSlide(index, wrapper, pagination, items.length);
    });
  });

  // 上一张按钮
  if (prevBtn) {
    prevBtn.addEventListener('click', (e) => {
      e.preventDefault();
      prevOwlSlide(wrapper, pagination, items.length);
    });
  }

  // 下一张按钮
  if (nextBtn) {
    nextBtn.addEventListener('click', (e) => {
      e.preventDefault();
      nextOwlSlide(wrapper, pagination, items.length);
    });
  }

  // hover显示按钮
  if (owlDemo && buttons) {
    owlDemo.addEventListener('mouseenter', () => {
      buttons.style.display = 'block';
    });
    owlDemo.addEventListener('mouseleave', () => {
      buttons.style.display = 'none';
    });
  }

  // 自动播放
  startOwlAutoPlay(wrapper, pagination, items.length);
}

// 更新owl slide显示
function updateOwlSlide(wrapper, pagination, totalItems, index) {
  // 计算translateX值
  const translateX = -index * itemWidth;
  wrapper.style.transform = `translate3d(${translateX}px, 0px, 0px)`;

  // 更新分页器
  pagination.forEach((page, i) => {
    if (i === index) {
      page.classList.add('active');
    } else {
      page.classList.remove('active');
    }
  });

  currentOwlSlide.value = index;
}

// 跳转到指定owl slide
function goToOwlSlide(index, wrapper, pagination, totalItems) {
  stopOwlAutoPlay();
  updateOwlSlide(wrapper, pagination, totalItems, index);
  startOwlAutoPlay(wrapper, pagination, totalItems);
}

// 上一张owl
function prevOwlSlide(wrapper, pagination, totalItems) {
  stopOwlAutoPlay();
  const newIndex = currentOwlSlide.value === 0 ? totalItems - 1 : currentOwlSlide.value - 1;
  updateOwlSlide(wrapper, pagination, totalItems, newIndex);
  startOwlAutoPlay(wrapper, pagination, totalItems);
}

// 下一张owl
function nextOwlSlide(wrapper, pagination, totalItems) {
  stopOwlAutoPlay();
  const newIndex = (currentOwlSlide.value + 1) % totalItems;
  updateOwlSlide(wrapper, pagination, totalItems, newIndex);
  startOwlAutoPlay(wrapper, pagination, totalItems);
}

// 开始owl自动播放
function startOwlAutoPlay(wrapper, pagination, totalItems) {
  stopOwlAutoPlay();
  owlAutoPlayTimer = setInterval(() => {
    nextOwlSlide(wrapper, pagination, totalItems);
  }, owlSlideInterval);
}

// 停止owl自动播放
function stopOwlAutoPlay() {
  if (owlAutoPlayTimer) {
    clearInterval(owlAutoPlayTimer);
    owlAutoPlayTimer = null;
  }
}
</script>

