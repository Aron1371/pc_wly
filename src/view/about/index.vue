<script setup>
import { ref, onMounted, onBeforeUnmount } from "vue";
import { loadCSSFiles, unloadCSS } from "../../utils/cssLoader";

// 动态加载 CSS（只在当前页面生效）
const componentId = 'about-page';
// 使用 import.meta.url 来获取正确的路径
const cssFiles = [
  new URL("../../assets/css/common.css", import.meta.url).href,
  new URL("../../assets/css/zsystem_menu.css", import.meta.url).href,
  new URL("../../assets/css/bootstrap.css", import.meta.url).href,
  new URL("../../assets/css/jqtree.css", import.meta.url).href,
  new URL("../../assets/css/brand.css", import.meta.url).href,
  new URL("../../assets/css/simplePagination.css", import.meta.url).href,
  new URL("../../assets/css/footer.css", import.meta.url).href
];

// tab切换状态：'list' 显示列表，'detail' 显示详情
const activeTab = ref("detail"); // 默认显示详情
// 当前激活的内容块（在brand-list或brand-detail内部）
const activeContent = ref("集团简介"); // 默认显示集团简介

// 切换到列表视图
function switchToList() {
  activeTab.value = "list";
}

// 切换到详情视图
function switchToDetail() {
  activeTab.value = "detail";
}

// 切换内容块
function switchContent(contentName) {
  activeContent.value = contentName;
}

// 初始化：为tab项和列表项添加点击事件
onMounted(() => {
  // 加载当前页面的 CSS
  loadCSSFiles(cssFiles, componentId);
  
  // viewport 已在路由守卫中设置，这里只需要确保设置正确（防止路由守卫执行失败）
  const isMobile = window.innerWidth <= 768;
  if (isMobile) {
    let meta = document.querySelector('meta[name="viewport"]');
    if (meta) {
      const deviceWidth = window.innerWidth || window.screen.width;
      const pageWidth = 1200;
      const calculatedScale = Math.min(deviceWidth / pageWidth, 1.0);
      const expectedContent = `width=${pageWidth}, initial-scale=${calculatedScale}, user-scalable=yes, maximum-scale=3.0, minimum-scale=0.1`;
      if (meta.content !== expectedContent) {
        meta.content = expectedContent;
      }
    }
  }
  
  // 为所有li.jqtree_common（包括父tab和子tab）添加点击事件
  const allTabItems = document.querySelectorAll("li.jqtree_common");

  allTabItems.forEach((tabItem) => {
    tabItem.addEventListener("click", (e) => {
      // 如果是父tab（jqtree-folder）
      if (tabItem.classList.contains("jqtree-folder")) {
        // 移除所有tab项的jqtree-selected类
        allTabItems.forEach((item) => {
          item.classList.remove("jqtree-selected");
        });

        // 给当前点击的父tab添加jqtree-selected类
        tabItem.classList.add("jqtree-selected");

        // 切换jqtree-closed类（展开/折叠）
        const toggler = tabItem.querySelector(".jqtree-toggler");
        if (tabItem.classList.contains("jqtree-closed")) {
          // 如果有jqtree-closed类，移除它（展开）
          tabItem.classList.remove("jqtree-closed");
          if (toggler) {
            toggler.classList.remove("jqtree-closed");
          }
          // 显示子菜单
          const subMenu = tabItem.querySelector("ul.jqtree_common");
          if (subMenu) {
            subMenu.style.display = "block";
          }
        } else {
          // 如果没有jqtree-closed类，添加它（折叠）
          tabItem.classList.add("jqtree-closed");
          if (toggler) {
            toggler.classList.add("jqtree-closed");
          }
          // 隐藏子菜单
          const subMenu = tabItem.querySelector("ul.jqtree_common");
          if (subMenu) {
            subMenu.style.display = "none";
          }
        }
      } else {
        // 如果是子tab（非folder）
        // 阻止事件冒泡，避免触发父tab的点击事件
        e.stopPropagation();

        // 移除所有tab项的jqtree-selected类
        allTabItems.forEach((item) => {
          item.classList.remove("jqtree-selected");
        });

        // 给当前点击的tab项添加jqtree-selected类
        tabItem.classList.add("jqtree-selected");

        // 获取菜单项文本
        const titleElement = tabItem.querySelector(".jqtree-title");
        if (titleElement) {
          const menuText = titleElement.textContent.trim();

          // 根据菜单项文本决定显示哪个视图和内容块
          // brand-list中的内容块（列表视图）
          if (
            menuText === "发展历史" ||
            menuText === "五粮荣誉" ||
            menuText === "档案文化" ||
            menuText === "企业文化" ||
            menuText === "宣传片" ||
            menuText === "十里酒城" ||
            menuText === "六大优势" ||
            menuText === "传承酿艺" ||
            menuText === "环保信息"
          ) {
            // 显示列表视图
            switchToList();
            // 设置对应的内容块
            switchContent(menuText);
          } else if (
            // brand-detail中的内容块（详情视图）
            menuText === "集团简介" ||
            menuText === "集团架构" ||
            menuText === "领导名单" ||
            menuText === "五粮液酒厂" ||
            menuText === "安吉集团" ||
            menuText === "进出口" ||
            menuText === "川橡国际" ||
            menuText === "保健酒公司" ||
            menuText === "生态酿酒公司"
          ) {
            // 显示详情视图
            switchToDetail();
            // 设置对应的内容块
            switchContent(menuText);
          } else {
            // 其他菜单项默认显示详情视图
            switchToDetail();
            switchContent("集团简介");
          }
        }
      }
    });
  });

  // 阻止所有内部a标签的点击效果
  const allLinks = document.querySelectorAll(".brand-list a, .brand-detail a");
  allLinks.forEach((link) => {
    link.addEventListener("click", (e) => {
      e.preventDefault(); // 阻止默认行为（跳转）
      e.stopPropagation(); // 阻止事件冒泡（防止触发tab切换）
      return false;
    });
  });
});

// 页面卸载时恢复 viewport 为默认设置，并移除当前页面的 CSS
onBeforeUnmount(() => {
  // 卸载当前页面的 CSS
  unloadCSS(componentId);
  
  // 恢复 viewport 为默认设置
  let meta = document.querySelector('meta[name="viewport"]');
  if (meta) {
    meta.content = "width=device-width, initial-scale=1.0";
  }
});
</script>

